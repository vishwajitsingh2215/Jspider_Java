package com;

class DoWhileLoopExample {
	public static void main(String[] args) {
		//do while loop for printing 1 2 3 4 5
		int i=1;
		do {
			System.out.println(i);
			i++;
		}
		while(i<=5);
		System.out.println("-------------");
		//do while loop for printing 5 4 3 2 1
		int j=5;
		do {
			System.out.println(j);
			j--;
		}
		while(j>=1);
	}

}
